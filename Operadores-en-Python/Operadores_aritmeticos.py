print("##INTRODUCIR DOS NUMEROS##")
primer_numero = int(input("ingresa numero: "))
segundo_numero = int(input("ingresa numero: "))

print(f"resultado de la suma entre los dos numero es: {primer_numero + segundo_numero}")
print(f"resultado de la resta entre los dos numero es: {primer_numero - segundo_numero}")
print(f"resultado de la multiplicacion entre los dos numero es: {primer_numero * segundo_numero}")
